List<DailyChoiceReferenceLink> _buildPlaceReferences(
  String queryZh,
  String queryEn,
) {
  return <DailyChoiceReferenceLink>[
    DailyChoiceReferenceLink(
      labelZh: 'OpenStreetMap 搜索建议',
      labelEn: 'OpenStreetMap search',
      url:
          'https://www.openstreetmap.org/search?query=${Uri.encodeComponent(queryZh)}',
    ),
    DailyChoiceReferenceLink(
      labelZh: '英文检索建议',
      labelEn: 'English search query',
      url:
          'https://www.openstreetmap.org/search?query=${Uri.encodeComponent(queryEn)}',
    ),
  ];
}

String _placeTitlePrefixZh(String distanceId) {
  return switch (distanceId) {
    'outside' => '附近',
    'nearby' => '同城',
    'travel' => '远一点的',
    _ => '',
  };
}

String _placeTitlePrefixEn(String distanceId) {
  return switch (distanceId) {
    'outside' => 'Nearby ',
    'nearby' => 'In-town ',
    'travel' => 'Further-out ',
    _ => '',
  };
}

