final List<DailyChoiceOption> _placeOptions =
    List<DailyChoiceOption>.unmodifiable(_buildPlaceOptions());

List<DailyChoiceOption> _buildPlaceOptions() {
  final options = <DailyChoiceOption>[];
  for (final scene in _placeSceneSeeds) {
    for (final distance in _placeDistanceProfiles) {
      for (final archetype in scene.archetypes) {
        options.add(_buildPlaceOption(scene, distance, archetype));
      }
    }
  }
  assert(
    _placeSceneSeeds.every((scene) => scene.archetypes.length == 8),
    'Each place scene should expose 8 archetypes.',
  );
  assert(
    options.length >= 300 && options.length <= 500,
    'Place options should stay between 300 and 500 items.',
  );
  return options;
}

