String _placeSceneTitleZh(String id) {
  return placeSceneCategories
      .firstWhere(
        (item) => item.id == id,
        orElse: () => placeSceneCategories.first,
      )
      .titleZh;
}

String _placeSceneTitleEn(String id) {
  return placeSceneCategories
      .firstWhere(
        (item) => item.id == id,
        orElse: () => placeSceneCategories.first,
      )
      .titleEn;
}

